package org.min.ex;

public class Ex04_String {

	public static void main(String[] args) {
		String str = "Java";
		System.out.println(str.toLowerCase());
		System.out.println(str.toUpperCase());
		System.out.println(str.indexOf('v'));  //<v는 몇번째 인덱스에 위치하는가 ?>
		System.out.println(str.charAt(2)); //<2번째 인덱스의 char값은 ?>
	}
}
